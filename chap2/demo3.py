# 整数可以表示为十进制 二进制 八进制 十六进制
# 0 - 9 逢10进1 - 默认进制
print('十进制', 11)
# 0 - 1 逢2进1 - ob
print('二进制', 0b11)
# 0 - 7 逢8进1 - 0o
print('八进制', 0o176)
# 0 - F 逢16进1 - 0x
print('十六进制', 0x1EAF)
