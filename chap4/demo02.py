# 以下对象为false
# False
# 数值 0
# none
# 空字符串
# 空列表 list
# 空元组 () tuple()
# 空字典 {} dict()
# 空集合 set()
print('---------- 以下对象的布尔值为 FALSE ---------- ')
print(False)
print(bool(0))
print(bool(None))
print(bool(''))
print(bool(""))
print(bool([]))
print(bool(list()))
print(bool(tuple()))
print(bool({}))
print(bool(set()))

